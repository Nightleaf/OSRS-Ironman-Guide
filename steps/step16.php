<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.15 - Starting Biohazard & More Barcrawl</h4>
<b>Location: </b> Port Sarim
<ol>
    <li>Barcrawl Port Sarim.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Pirate's_Treasure" target="_blank">Pirate's Treasure</a> until you need to go to Varrock.</li>
    <br>
    <li>Teleport to Varrock.</li>
    <li>Get pirate's message from Blue Moon inn.</li>
    <li>Use the Spirit tree to travel to Gnome Stronghold.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/The_Grand_Tree" target="_blank">Grand Tree</a>.</li>
    <li>Buy a premade blurb' sp, 2 vodka, pineapple chunks.</li>
    <p>
	Non-efficient method:<br><br>
	   If you're playing more casually and don't plan on doing barbarian fishing for agility xp, train 40 agility at Varrock course now. 
    </p>
    <li>Use the Spirit tree to travel to Ardougne.</li>
</ol>
<b>Location: </b>Ardougne
<ol>
    Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>Scrying orb</li>
        <li>Pickaxe</li>
        <li>Fire strike runes</li>
    </ul>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Biohazard" target="_blank">Biohazard</a>, keep the pots and sample in bank.</li>
    <li>Visit essence mines, scrying orb 3/3.</li>
    <li>Buy 11 breads from cake stall.</li>
    <li>Mine 2 iron ore. (If you got some from Wintertodt: ignore this step)</li>
</ol>