<h3>4.0 Skilling, lots of it</h3>
<ol>
    <li>Teleport to Camelot.</li>
    <li>Plant barley seeds at the hops patch north of Seers, pay the farmer 3 normal compost to look after them.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Giant_Dwarf" target="_blank">The Giant Dwarf</a> quest.</li>
    <li>Buy emeralds from gem store until log in limit.</li>
    <p>These next 2 quests have times where you have to wait for stuff to happen, smith iron dart tips while you wait</p>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Forgettable_Tale..." target="_blank">Forgettable Tale of a Drunken Dwarf</a></li>
    <p>
        There's an anvil near the bank in keldagrim.
        <br><br>
        Your barley should be grown when you need it, use it on a range to get the barley malt
    </p>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Garden_of_Tranquillity" target="_blank">Garden of Tranquillity</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Enlightened_Journey" target="_blank">Enlightened Journey</a>.</li>
    <p>
    While waiting for the willow tree to grow:
    <br><br>
        Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Eyes_of_Glouphrie" target="_blank">Eyes of Glouphrie</a> and <a href="http://oldschoolrunescape.wikia.com/wiki/Tower_of_Life">Tower of Life</a> (Should get you 15 construction, which allows you to make oak stuff in the workshop)
        Smith the rest of your iron dart tips, leave 20 bars for quests
    </p>
    <li>Go to Shilo Village.</li>
    <li>Mine and cut 5 opals/jade/red topaz, doesn't matter which gems as long as you get 5.</li>
    <p>Don't mine any more than that since it's slow without a charged glory.</p>
    <li>Talk to Murcaily to start Tai bwo wannai cleanup minigame and repair 1 part of the fence to be able to cut teaks.</li>
    <li>Sell the gems to Gabooty in tai bwo wannai for trading sticks.</li>
    <li>Cut teaks in the grove until 60 woodcutting.</li>
    <br>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Enlightened_Journey" target="_blank">Enlightened Journey</a> if you haven't already.</li>
    <p>Unlock whichever balloon routes you might want to use, I use castle wars -> taverly for tree runs.</p>
    <li>Finish 99 Fishing.</li>
    <p>
	Do farm runs while you fish, at least tree runs if you have seeds from kingdom and the harralander seeds from master farmer.
    <br><br>
	If you need a break then feel free to do the construction and hunter parts that are next in the guide.
	Buy the cape, unlimited teles close to a bank
    </p>
</ol>