<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.9 - Trip to Mage Bank</h4>
<ol>
    <li>Walk to Edgeville.</li>
    <li>Equip a slash weapon. (Bronze sword is fine)</li>
    <li>Walk to Mage Bank.</li>
    <p>
        Be ready to log out if you see a white dot, dying is a waste of time.
        <br>
        <br>
        <font color="#853f3d"><b>HCIM: The odds of running into a lvl60 pker is extremely low, but i'd still recommend scouting the lever with another acc first.
                If you really don't want to risk it then it's fine, but I'm gonna assume while making this guide that you have some laws. You'll
                most likely get some laws as a drop before you can use them.</b></font>
    </p>
    <li>Buy ~50 law runes for now and all of the cosmic runes that you can get</li>
    <p>Recommend taking a 5min break after you hit the log-in limit just to get a few more cosmics, but that's up to you.</p>
    <li>Home teleport to Lumbridge.</li>
    <li>Pick up a few logs and fletch them into headless arrows, make bronze arrows for 10 fletching.</li>
    <li>Get your GP and walk to Al-Kharid.</li>
    <li>Buy 20 thread and all of the moulds from the crafting shop.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Prince_Ali_Rescue" target="_blank">Prince Ali Rescue</a>.</li>
    <li>Buy 1 inventory of bronze bars for questing from Shantay Pass.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Tourist_Trap" target="_blank">The Tourist Trap</a>.</li>
    <p>
        Safespot for the captain: <a href="https://www.youtube.com/watch?v=VFxB4zWqrd8" target="_blank">VIDEO</a>.
        <br>
        <b><font color="red">NOTE:</font></b> Make sure you put both XP rewards on AGILITY!
    </p>
</ol>