<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.2 - Starting quests in members areas & thieving</h4>
<ol>
    <li>Walk to Falador and start <a href="http://oldschoolrunescape.wikia.com/wiki/The_Knight%27s_Sword" target="_blank">The Knight's Sword</a> by speaking to the squire.</li>
    <br>
    <li>Use the Castle Wars minigame teleport.</li>
    Fill your inventory with the following:
    <ul>
        <li>Steel Axe</li>
        <li>55 gp</li>
        <li>Tinderbox</li>
        <li>Jug of Water</li>
        <li>1 normal log</li>
    </ul>
    <br>
    <li>Walk to Yanille.</li>
    <li>Open the hunter shop to complete a step in the Ardougne easy diary.</li>
    <li>Buy a pie dish and a chocolate bar at the Yanille cooking shop.</li>
    <p>Crush the chocolate with a knife as a requirement for Plague City.</p>
    <br>
    <li>Walk to Port Khazard</li>
    <li>Buy swamp paste from the general store.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Monk%27s_Friend" target="_blank">Monk's Friend</a>.</li>
    <br>
    <li>Get 35 woodcutting at oak trees and firemake the logs as you cut them.</li>
    <p><a href="http://i.imgur.com/EN5Jpdb.png" target="_blank">Location of Oak Trees</a> </p>
</ol>

<b>Location: </b> Ardougne
<ol>
    <li>Get 25 Thieving from the Cake/Silk stalls.</li>
    <p>Make sure you have at least 100 cakes stored in your bank.</p>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Sheep_Herder" target="_blank">Sheep Herder</a>.</li>
    <li>Sell 6 cakes to the general store for the 100 gp.</li>
</ol>
