<h3>Skilling, lots of it</h3>
<h4>4.2 - Regicide & Fletching</h4>
<ol>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Temple_of_Ikov" target="_blank">Temple of Ikov</a> quest.</li>
    <p>Start fletching iron darts while you run around doing quests.</p>
    <li>Do the <a href="http://oldschoolrunescape.wikia.com/wiki/Regicide" target="_blank">Regicide</a> quest.</li>
    <p>Pick up a few whiteberries just north of the elf tracker.</p>
    <li>If you have enough sand and soda ash, make molten glass until 46 crafting and then blow them into orbs for 49 craft
	If you don't have enough then go buy them now at Catherby (most likely more in stock than khazard)</li>
    <li>Do Hand in the sand quest, get daily sand every day.</li>
    <br>
    <li>Do these quests while fletching darts:</li>
    <ul>
        <li><a href="http://oldschoolrunescape.wikia.com/wiki/Big_Chompy_Bird_Hunting" target="_blank">Big Chompy Bird Hunting</a></li>
        <li><a href="http://oldschoolrunescape.wikia.com/wiki/Family_Crest" target="_blank">Family crest</a></li>
        <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Legends%27_Quest" target="_blank">Legend's quest</a> (for RFD. You can finish this whenever you want, I probably won't give it a special mention later)</li>
        <li><a href="http://oldschoolrunescape.wikia.com/wiki/Death_Plateau_(quest)" target="_blank">Death plateau</a></li>
        <li><a href="http://oldschoolrunescape.wikia.com/wiki/Troll_Stronghold" target="_blank">Troll Stronghold</a></li>
    </ul>
    <li>I'll assume you have fletched everything now and have 53 fletching</li>
    <p>
        It depends how many days have passed since you unlocked kingdom, but I'd guess you have around 8k maples now.
    </p>
    <li>Fletch maple shortbows (u) until 55 fletching.</li>
    <li>Fletch all of your maple logs into maple longbows (u).</li>
    <p>If you want to be as efficient as possible, buy craft supplies and fletch to regen run energy</p>
    <li>Buy same amount of nats (from mage bank, get cosmics too) as maple longs(u) and alch them while doing agility</li>
    <p><font color="#853f3d"><b>HCIM: Don't buy too many at once if you're buying from Ali since you can go buy them from mage guild for cheaper at 63 mage.</b></font></p>
    <li>Do agility until full graceful, switch to Rellekka at 80 agility.</li>
    <p>Loot kingdom and fletch more longbows to alch if you run out. Finish graceful even if you don't have enough alchs.</p>
    <li>Guessing you have 81 agility and around 70 mage now, the goal is 77 for superglass make. </li>
    <li>Keep alching maple (u)'s while you do farm runs and quests.</li>
    <br>
    <li>Do all of the RFD subquests, except the awowogei part.</li>
    <li>Buy a bunch of chocolate dust from the chest and make energy pots.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Monkey_Madness_I" target="_blank">Monkey Madness I</a>, focus reward on attack and defense.</li>
    <li>Awowogei sub quest (70 cooking at the Hosidius mess hall if you forgot where to get it)</li>
    <br>
    <li>Get crafting supplies for 61 crafting.</li>
    <p>
    	From 49 you need 2920 buckets of sand and soda ash (58.4k from making molten glass and 153.3k from blowing orbs)
    <br><br>
	You can save the seaweed for later and just buy the supplies until 2.9k buckets and ash, or you can cook the seaweed and fill sand
	manually. If you fill sand, put your house to yanille and bank with fishing cape. Set house tele to outside.
    </p>
    <li>Get 61 Crafting.</li>
    <p>
    	If you bought your nats at mage bank and got some cosmics, you can charge fire orbs for later while you craft orbs. House to 
	Taverly and bank with dueling rings or fish cape. Rune pouch is extremely helpful for this.
    </p>
</ol>