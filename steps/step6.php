<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.5 - Wintertodt & Gertrude's Cat</h4>
<ol>
    <li>Do Wintertodt until 75 Firemaking.</li>
    <p>	You really shouldn't be in any danger of dying with 10hp, but pay attention anyway, it's a long walk back if you die.
        <br><br>
        10HP accounts start taking 1 damage at 50 FM even without warm clothing, but if you dug up the clue hunter boots and gloves earlier
        then you might as well use them.
        <br><br>
        Alternative: If you aren't planning on killing Zulrah for magic logs to get 99 Firemaking, you can do Wintertodt until 99 now if you want
        to.
        <br><br>
        You will miss out on a fair bit of supplies for not having higher other skills, but it's not the end of the world.  You don't
        need to do WT for money, you'll get plenty later on and it's only 1m gp from 99FM.
    </p>
    <br>
    <li>Home teleport to Lumbridge.</li>
    <li>Grab 3 buckets and a few cakes to be safe.</li>
    <br>
    <li>Walk north to Fred's farm.</li>
    <li>Kill a chicken for the raw chicken.</li>
    <li>Pick up an egg to the north of Fred.</li>
    <li>Kill 1 Cow Calf for meat, keep the bones for Demon Slayer.</li>
    <li>Fill 3 buckets with Milk.</li>
    <br>
    <li>Walk to Barbarian village (you can walk east of draynor manor by the river, don't walk around through falador or something like that)</li>
    <li>Get the ammo mould and the notes for Dwarf Cannon from Nulodion.</li>
    <li>Make your way to West Varrock to start <a href="http://oldschoolrunescape.wikia.com/wiki/Gertrude's_Cat" target="_blank">Gertrude's Cat</a>.</li>
    <li>Pickup Doogle leaves, and use the leaves on a sardine.</li>
    <li>Grab a bucket, your barcrawl card, the Rune Mysteries package and GP from the bank.</li>
    <br>
    <li>Continue Gertrude's Cat and talk to the kids in Varrock Square.</li>
    <li>Speak to Romeo to begin <a href="http://oldschoolrunescape.wikia.com/wiki/Romeo_%26_Juliet" target="_blank">Romeo & Juliet</a>.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Demon_Slayer">Demon Slayer</a> and get the first two keys.</li>
</ol>