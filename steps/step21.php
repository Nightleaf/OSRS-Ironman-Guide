<h3>2.0 - Thieving, fishing and mining</h3>
<b>Location: </b>Desert
<ol>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/The_Feud" target="_blank">The Feud</a> at Ali Morrisane, buy the desert disguise.</li>
    <li>Smelt the 5 silver, make a sickle and unstrung holy symbol, keep 3 bars.</li>
    <li>Talk to Osman to get the key for Prince ali rescue quest</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Feud" target="_blank">The Feud</a>, get <a href="http://oldschoolrunescape.wikia.com/wiki/Maple_blackjack" target="_blank">Maple blackjack</a>.</li>
    <p><font color="#853f3d"><b>HCIM: Unlock the rune shop also, it's a much safer option than mage bank before mage guild (Although you won't get as many cosmics).</b></font></p>
    <li>Blackjack until 80 thieving, you can buy wines from the bar to use as food.</li>
    <p>
    If you've been raising cats so far, I recommend not to while blackjacking because it gives you a delay when the kitten says it's hungry or
	wants attention. This could cause the bandit to attack you, or even kill you.
    </p>
    <li>Use your Ardougne cloak teleport and take the spirit tree to Gnome Stronghold.</li>
        Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>2 Bird snares</li>
        <li>5 Butterfly jars</li>
        <li>Butterfly net</li>
        <li>Axe</li>
        <li>Knife</li>
    </ul> 
    <li>Go out of the stronghold and walk by the fence to the west and then go north to Piscatoris</li>
    <br>
    <li>Get 29 hunter.</li>
    <p>
    Put your bird snare right here: <a href="http://i.imgur.com/hQutEIn.png" target="_blank">IMAGE</a>.
    <br><br>
	Catch just the 1 bird until level 15, at that point start catching the ruby harvest butterflies, while hunting birds at the same
	time.
    <br><br>
    You can put another bird snare at level 20. Doing this should get you 29hunt in less than 90 minutes.
    <br><br>
    If you're planning on doing 3-tick skilling, get a full inv of kebbit claws. 
    <br><br> 
    My preferred spot: <a href="http://i.imgur.com/QebW6Yc.png" target="_blank">IMAGE</a>.
    <br><br>
	Need 32 craft to make spiked vambs, so just make some molten glass. Kill cows for leather since you need 40 range to buy them.    
    </p>
</ol>