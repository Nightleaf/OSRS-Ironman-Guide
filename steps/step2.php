<b>Location: </b>Lumbridge Swamp
<ol>
    <li>Kill a rat (safespot with ranged) for its meat, keep bones for demon slayer.</li>
    <li>Take a spade from the swamp shed that takes you to Zanaris.</li>
    <li>Pick up all of the swamp tar you see. (at least 5)</li>
    <li>Get the ghostspeak amulet and equip it.</li>
    <li>Pick up cheese near Aggie.</li>
    <li>Take air talisman to wizard tower</li>
    <li>Walk back and go to Rimmington, pick up 1 snape grass near crafting guild.</li>
</ol>
