<h3>Thieving, fishing and mining</h3>
<h4>2.1 - Getting 70 Agility & Rogues Outfit</h4>
<ol>
    <li>Spirit tree to gnome stronghold and take the glider to Karamja.</li>
    <li>Go to Shilo Village.</li>
    <li>Buy 50-100k feathers for now (Or 250k if you know for sure you're doing 99 fishing eventually at barb fish, ~150k if knife 3t method)</li>
    <li>Get 58 fishing with trout and salmon.</li>
    <br>
    <li>Use the minigame teleport to barbarian assault.</li>
    <li>Get 70 agility from barbarian, should be around 94 fishing.</li>
    <p>
        You can feed caviar and roe to kitten if you want death runes, just saves gp so it's not that important.
        Finish 99 fishing after you unlock kingdom, 70 agi and str is all you really need for now
        <br><br>
        Alternate method: If you don't want to fish, do priest in peril now and train 52 agility, go to wildy course for 60 and then get
        full graceful and 70 agi at seers course. You don't have to do this all at once of course but you need at least 70 eventually.
        <br><br>
        <font color="#853f3d"><b>HCIM: Stick to Canifis course until 60 agility.</b></font>
        
        <br><br>
        That being said I HIGHLY recommend fishing at least 50 agility, since you need the higher strength level very soon anyway.     
    </p>
    <li>After 70 agility, use the minigame teleport to Burthorpe.</li>
    <li>Do the rogue's den minigame for full rogue outfit.</li>
    <p>
        Rogue's Den guide by Lelador: <a href="https://www.youtube.com/watch?v=c5tQewEZPe4" target="_blank">VIDEO</a>
        <br><br>
        You can cook the trout and salmon while you regen run energy for maximum efficiency
	      
    </p>
    <li>Buy 50 lockpicks while you're here</li>
    <li>Equip the set and go back to thieving until around 2.5m GP</li>
    <li>Carpet back to Shantay pass.</li>
    Fill your inventory with the following:
    <ul>
        <li>Teleport runes</li>
        <li>All <a href="http://oldschoolrunescape.wikia.com/wiki/The_Golem" target="_blank">The Golem</a> quest items</li>
    </ul>  
</ol>