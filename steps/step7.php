<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.6 - Shield of Arrav & Continuing Barcrawl</h4>
<ol>
    <li>Talk to Reldo in the Varrock castle library for the knight's sword quest.</li>
    <li>Do Shield of Arrav as soon as possible on any visit to Varrock.</li>
    <p>To find partners from the other gang, use the minigame tab under quests to join the clan chat.</p>
    <li>Buy gloves and a pink skirt from the clothes shop in the Varrock square.</li>
    <li>Buy a rotten tomato from the crate near the achievement diary guy.</li>
    <li>Do barcrawl Varrock 1/2</li>
    <li>Buy 3 beers.</li>
    <li>Walk to the Varrock museum basement and do the <a href="http://oldschoolrunescape.wikia.com/wiki/Natural_history_quiz">Natural History Quiz</a> for 9 hunter and slayer.</li>
    <li>Continue Rune Mysteries.</li>
    <li>Buy the following items:</li>
    <ul>
        <li>Staff of Fire</li>
        <li>100 fire runes</li>
        <li>300 earth runes</li>
        <li>300 water runes</li>
        <li>4000 mind runes</li>
        <li>8000 air runes</li>
    </ul>
    Fill your inventory with the following:
    <ul>
        <li>100 gp</li>
        <li>Barcrawl card</li>
        <li>Bucket of milk</li>
        <li>Seasoned Sardine</li>
        <li>Cakes</li>
    </ul>
    <li>Walk towards Edgeville.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Enter_the_Abyss">Enter the Abyss</a> miniquest by talking to the Zamorak mage in the wilderness.</li>
    <br>
    <li>Get 2 Iron bars from the wilderness from this location: <a href="http://i.imgur.com/JKAil41.png" target="_blank">IMAGE</a>.</li>
    <p><font color="#853f3d"><b>HCIM: this is fairly safe, just be ready to log out if you see a white dot.</b></font></p>
    <li>Walk to the pub north-east of Varrock.</li>
    <li>Do the final pub for barcrawl in Varrock. (2/2)</li>
    <li>Continue Gertrude's cat at the lumberyard north east of Varrock.</li>
    <li>Grab a pickaxe from the east bank in Varrock.</li>
    <br>
    <li>Get Cadava berries, 12 redberries and enough to make a redberry pie (Get 3+ in-case you burn the pie)</li>
    <p>Both berries have a spawn near the Varrock east mining area, you can world hop to get berries quicker.</p>
    <li>Mine 1 tin and 5 copper ore.</li>
</ol>