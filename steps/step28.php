<h3>Fairy rings, 43 prayer, kingdom and first 99</h3>
<h4>3.4 - Throne of Miscellania & 99 Thieving</h4>
<ol>
    <li>Upgrade the Iban Staff.</li>
    <li>Kill sand crabs for 50 attack to wield the iban's staff, shouldn't take more than one hour.</li>
    <p>
    If you have a slayer task which doesn't require melee prot then you can do that instead too.
    </p>
    <li>Kill 85 blue dragons with iban's blast, don't bother banking the hides other than maybe 6 to make full set later.</li>
    <br>
    <li>Use the fishing trawler minigame teleport.</li>
    <li>Buy 90 buckets of slime, and all sand/soda ash you can get.</li>
    <li>Equip the Ghostspeak amulet.</li>
    <li>Prepare the nettle water for <a href="http://oldschoolrunescape.wikia.com/wiki/Ghosts_Ahoy" target="_blank">Ghosts Ahoy</a>.</li>
    <br>
    <li>Make sure you have all items for <a href="http://oldschoolrunescape.wikia.com/wiki/Ghosts_Ahoy" target="_blank">Ghosts Ahoy</a>.</li>
    Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>9 Dragon bones</li>
        <li>9 Pot</li>
        <li>9 Slime</li>
        <li>Pots (General store sells these)</li>
    </ul>
    <li>Charter to Port Phasmatys.</li>
</ol>
<b>Location: </b>Port Phasmatys/Canifis
<ol>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Ghosts_Ahoy" target="_blank">Ghosts Ahoy</a>.</li>
    <li>Grind 9 d bones and offer them, get ecto tokens</li>
    <li>Finish Ghosts Ahoy.</li>
    <li>Grind the rest of your bones, should get 43 prayer.</li>
    <br>
    <li>Get 40 slayer.</li>
    <p>
    	Train range to 40 first and then just start training attack with a rune sword
	If you get a shade task then do the next step during that task
    </p>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Shades_of_Mort%27ton" target="_blank">Shades of Mort'ton</a> quest.</li>
    <li>Take the book to the apothecary in Varrock for some free herb xp.</li>
    <li>Go talk to Historian Minas in the 2nd floor of varrock museum, use lamps on herblore.</li>
    <br>
    <li>Get 25 Herblore (If you don't already have it)</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Heroes%27_Quest" target="_blank">Heroes' Quest</a>.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Fremennik_Trials" target="_blank">Fremennik Trials</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Throne_of_Miscellania">Throne of Miscellania</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Royal_Trouble">Royal Trouble</a>.</li>
    <br>
    <li>Put at least 1m gp to kingdom, set it on 10 herbs and 5 maples</li>
    <p>
        Get 100% favor back everyday, loot it every 3 days to get the most birds nests.
    </p>
    <br>
    <li>Finish 99 Thieving.</li>
    <li>Buy a Thieving Cape.</li>
    <li>Thieve master farmers for a few hours to get a decent amount of seeds for farming.</li>
