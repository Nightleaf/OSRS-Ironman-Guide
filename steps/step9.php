<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.8 - Taverly Quests & Redberry Pie</h4>
<ol>
    <li>Walk to Taverly.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Witch%27s_House" target="_blank">Witch's House</a>.</li>
    <p>Tank the first 2 phases on the north side of the shed and run to safespot when you need to eat.
        <br>
        <br>
        It's possible to safespot the first 2 phases, but with fire strike you are in no real danger of dying with cakes.
    </p>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Druidic_Ritual" target="_blank">Druidic Ritual</a>.</li>
    <li>Buy 2 eyes of newt and a pestle and mortar in Taverly.</li>
    <br>
    <li>Walk to Falador.</li>
    Fill your inventory with the following:
    <ul>
        <li>Pickaxe</li>
        <li>Eye of newt</li>
        <li>2 Iron bars</li>
        <li>Bucket (of water></li>
        <li>Pie dish</li>
        <li>Redberries</li>
        <li>Some food</li>
    </ul>
    <li>Buy 500 feathers from Port Sarim.</li>
    <li>Make a redberry pie.</li>
    <p>You can buy flour and fill bucket with water at Port Sarim.</p>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Witch%27s_Potion" target="_blank">Witch's Potion</a> quest.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/The_Knight's_Sword" target="_blank">The Knight's Sword</a> quest.</li>
    <li>Mine 2 Blurite ore. (One is for achievement diary)</li>
</ol>