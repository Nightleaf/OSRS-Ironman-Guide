    <li>Make a ring of wealth to use during slayer.</li>
    <p><font color="#853f3d"><b>HCIM: Might wanna use ring of life, although wealth makes shield left half a lot more common.</b></font></p>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Pest_Control" target="_blank">Pest control</a> for full range void in the intermediate boat, train at least 55 range with a bone crossbow at PC</li>
