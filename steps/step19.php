<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.18 - Getting Ardougne Cloak 1</h4>
<b>Location: </b>Varrock
<ol>
    <li>Continue <a href="http://oldschoolrunescape.wikia.com/wiki/The_Lost_Tribe" target="_blank">The Lost Tribe</a> until the need to go to the Goblin Village.</li>
    <li>Buy priest robes(keep these after quest), boots, gloves(if you don't have)</li>
    <br>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Enter_the_Abyss" target="_blank">Enter the Abyss</a>.</li>
    <li>Get the magic words for <a href="http://oldschoolrunescape.wikia.com/wiki/Merlin%27s_Crystal" target="_blank">Merlin's Crystal</a> at the altar.</li>
    <li>Continue <a href="http://oldschoolrunescape.wikia.com/wiki/Biohazard" target="_blank">Biohazard</a>.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/The_Dig_Site" target="_blank">The Digsite Quest</a>.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Special:Search?query=Dragon+Slayer">Dragon Slayer</a>, mine  5 silver ore near Champions Guild.</li>
    <li>Use the Spirit Tree to travel to Ardougne.</li>
</ol>
<b>Location: </b>Ardougne
<ol>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Biohazard" target="_blank">Biohazard</a>.</li>
    <li>Finish any easy tasks for the Ardougne achievement diary, then get the cape.</li>
    <p>
        You won't have 30herb for the lamp so just use it on agility or something.
    </p>
    <li>Trade cat for 200 death runes.</li>
    <br>
    <li>Use the fishing trawler minigame teleport.</li>
    <li>buy sand/soda ash/seaweed until you have 15k GP left OR log in limit</li>
    <br>
    <li>Walk to Ardougne.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Jungle_Potion" target="_blank">Jungle Potion</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Shilo_Village" target="_blank">Shilo Village</a>.</li>
    <p><font color="#853f3d"><b>HCIM: Buy a few karambwans and swordfish from brimhaven food store, be very careful in the last multicombat dungeon, it's probably
