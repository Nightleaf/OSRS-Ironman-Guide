<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.3 - Starting Barcrawl & Waterfall</h4>
<ol>
    <li>Take out GP and a small fishing net.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Sea_slug" target="_blank">Sea Slug</a>.</li>
    <p>Make sure you fish shrimp while doing the quest to complete a step for diary.</p>
    <li>Buy 1 inventory of sardine from the fish shop in Witchaven.</li>
    <li>Sell silk but keep at least 10 in your bank for future quests.</li>
    <p>You should have 10k gp at this point.</p>
    <li>Buy a player owned house.</li>
    <li>Buy rope, 5 vials, 30 balls of wool, and 7 papyrus from the Ardougne general store.</li>
    Fill your inventory with the following:
    <ul>
        <li>Rope</li>
        <li>Spade</li>
        <li>gp</li>
        <li>A few cakes</li>
    </ul>
    <li>Dig up Clue Hunter gloves & boots just north of Ardougne.</li>
    <p>Location: <a href="http://i.imgur.com/sOD5Zhz.png" target="_blank">IMAGE</a></p>
    <li>Buy greenman's ale from Rasolo.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Dwarf_Cannon" target="_blank">Dwarf Cannon</a> until you have to go to Ice Mountain.</li>
    <br>
    <li>Pickup planks at Barbarian Assault until the login limit.</li>
    <p>If there happen to be no bots taking them, then you might not log out at all. Not sure how many you need in total but just get like
        150 max for now.</p>
    <li>Do the Barbarian Assault tutorial to unlock the minigame teleport.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Alfred_Grimhand%27s_Barcrawl" target="_blank">Alfred Grimhand's Barcrawl</a> miniquest.</li>
    <li>Do the <a href="http://oldschoolrunescape.wikia.com/wiki/Waterfall_Quest" target="_blank">Waterfall Quest</a> until the Gnome Maze.  Make sure to read the book!</li>
</ol>