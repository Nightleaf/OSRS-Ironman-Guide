<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.4 - Barcrawl Grand Tree & Gathering more supplies</h4>
<ol>
    <li>Walk to Gnome Stronghold.</li>
    <li>Do the barcrawl step for grand tree.</li>
    <li>Buy Dwellberries from Heckel Funch near the bar.</li>
    <li>Buy 450 bronze arrowheads. (100 from each world)</li>
    <li>Buy fruit blast items; pineapple, orange, lemon, lemon slice, cocktail shaker, cocktail glass.</li>
    <br>
    <li>Walk to Ardougne.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Plague_City" target="_blank">Plague City</a>.</li>
    <br>
    <li>Use the Castle Wars minigame teleport.</li>
    <li>Take out your steel axe and a tinderbox.</li>
    <li>Run south-west of castle wars to this location: <a href="http://i.imgur.com/cOzCs3x.png">IMAGE</a>.</li>
    <li>Cut and firemake teak logs until you hit 50 firemaking.</li>
    <br>
    <li>Home teleport back to Lumbridge.</li>
    <li>Talk to the ghost for Restless Ghost quest.</li>
    <li>Walk to Port Sarim and take the boat to Great Kourend. (Veos is the one you talk to)</li>
    <li>Make your way north to Wintertodt.</li>
</ol>