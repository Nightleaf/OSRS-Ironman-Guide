<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.7 - Raising Cats</h4>
<ol>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Gertrude's_Cat" target="_blank">Gertrude's Cat</a>.</li>
    <p>Keep raising cats with the sardines you got earlier.  Make sure to read about how to get your cat to grow: <a href="http://oldschoolrunescape.wikia.com/wiki/Cat" target="_blank">Cat Wiki Page</a>.</p>
    <li>Talk to Juliet.</li>
    Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>Runes</li>
        <li>Barcrawl card</li>
    </ul>
    <li>Equip your Staff of Fire.</li>
    <br>
    <li>Walk towards Falador.</li>
    <li>Get 13 magic at barbarians by safe spotting over the rocks.</li>
    <li>Buy a bronze med helm from Peksa.</li>
    <li>Kill a bear with fire strike for its meat near mind altar.</li>
    <br>
    <li>Walk to Falador and do barcrawl.</li>
    <li>Buy 5 Wizard mind bombs from the pub.</li>
    <li>Walk to the garden and buy 6 woad leaves, offer 20 gp to get 2 leaves.</li>
    <li>Bank your items.</li>
    Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>Cheese</li>
        <li>Fire strike runes</li>
        <li>all 4 kinds of meat for <a href="http://oldschoolrunescape.wikia.com/wiki/Druidic_Ritual" target="_blank">Druidic Ritual</a>.</li>
        <li>18 Cakes</li>
    </ul>
    <li>Equip gloves.</li>
    <br>
</ol>