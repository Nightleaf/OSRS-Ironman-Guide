<h3>Thieving, fishing and mining</h3>
<h4>2.2 - Elemental Workshop II & Continuing Dragon Slayer</h4>
<ol>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/The_Golem" target="_blank">The Golem</a>.</li>
    <li>Start <a href="" target="_blank">Elemental Workshop II</a> by reading the book while at Digsite, keep the items in the bank.</li>
    <li>Finish the golem, get the gems with chisel and hammer.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Shadow_of_the_Storm" target="_blank">Shadow of the Storm</a>. (should have priest gown and black wizard hat in bank as your black items)</li>
    <p>
        10K REWARD ON RANGED ALWAYS!
        <br><br>
        If you didn't do fishing for str lvls, this boss fight will take you like +30mins of flinching. In that case probably worth
		leaving this quest for later, or think of some way you can get your melee stats up.
    </p>
    <li>Go to Edgeville and talk to Oziach to continue <a href="http://oldschoolrunescape.wikia.com/wiki/Dragon_Slayer" target="_blank">Dragon Slayer</a>.</li>
    <li>Go to mage bank and buy 6k nats, all of the cosmics you can get and 10 law runes each world.</li>
    <p>
    	This might take more than 1 trip. HCIM can buy them from ali morrisane if you don't wanna risk mage bank, although they're more
	expensive.
    </p>
    <li>Teleport to Falador.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Pirate%27s_Treasure" target="_blank">Pirate's Treasure</a> quest.</li>
    <li>Get 2 buckets of sap from this tree near falador east bank.  Location: <a href="http://i.imgur.com/YoH5z70.png" target="_blank">IMAGE</a></li>
    <li>Buy adamant and rune picks from the pickaxe shop in the mines.</li>
    <br>
    <li>Mine 3400 iron ore at ardy monastery, superheat them while walking to Ardy bank and tele back with ardy cloak.</li>
    <p>
    While mining, you can tele to Khazard every now and then to buy craft supplies.
        <br><br>
	Do medium clues if you get them from mining, might get a yew bow from reward (Comp bow works too, it's for temple of ikov)
    </p>
    <li>Don't smith them into anything yet.</li>
</ol>