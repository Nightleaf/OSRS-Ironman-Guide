<h3>5.0 - Diaries and finishing RFD</h3>
<ol>
    <li>Buy 2.2k gold ore from Blast furnace shop and get 60 smithing.</li>
    <p>You need to pay the foreman 10k every now and then when below 60.</p>
    <li>Make a mithril grapple.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Watchtower" target="_blank">Watchtower</a> quest</li>
    <li>Do Ardougne medium diaries, start getting daily essence. </li>
    <p>100 might not seem like much but it adds up, always buy sapphires and emeralds and thieve stall while you go get it daily.</p>
    <br>
    <li>Do these quests for QP and diaries:</li>
    <ul>
        <li>Icthlarin's little helper</li>
        <li>Eagles' peak</li>
        <li>Enakhra's lament</li>
        <li>Rat catchers</li>
        <li>Olaf's quest</li>
        <li>Between a rock</li>
        <li>Tai bwo wannai trio</li>
        <li>Rum deal & Cabin fever (Req 42 slayer, if you got a wild pie from eclectics then do this, if not then leave morytania medium for later)</li>
        <li>A soul's bane</li>
        <li>One small favour</li>
    </ul>
    <br>
    <li>Do all easy and medium diaries.</li>
    <p>
	Wildy requires 50 Slayer, leave that for when you go back to slayer.
        
    <br><br>
	Buy cosmics from mage bank for now to charge orbs for daily staffs 4k cosmics is enough to last until you can get 59 RuneCrafting
    </p>
    <li>Do Eadgar's ruse.</li>
    <li>Do My arm's big adventure</li>
    <li>Do Desert Treasure</li>
    <br>
    <li>Finish RFD, buy Barrows gloves</li>
