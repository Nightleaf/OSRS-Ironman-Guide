<h3>Early quests & Ardougne cloak 1</h3>
<h4>1.19 - Boots of Lightness</h4>
<ol>
    <li>Use the Fishing trawler minigame teleport.</li>
    <li>buy sand/soda ash/seaweed until you have 15k GP left OR log in limit.</li>
    Fill your inventory with the following:
    <ul>
        <li>Pickaxe</li>
        <li>Lit candle</li>
        <li>Excalibur</li>
        <li>Bat bones</li>
        <li>Black candle</li>
        <li>Tinderbox</li>
        <li>Knife</li>
    </ul>
    <li>Teleport to Camelot.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Merlin%27s_Crystal" target="_blank">Merlin's Crystal</a>.</li>
    <li>Start <a href="http://oldschoolrunescape.wikia.com/wiki/Holy_Grail" target="_blank">Holy Grail</a>.</li>
    <br>
    <li>Get Boots of lightness.</li>
    <li>Mine 12 coal near Legend's Guild.  (If you got 12 coal from Wintertodt, skip this step)</li>
    <li>Buy 1 inventory of stew from the Seer's Village bar.</li>
    <li>Do Elemental Workshop I.</li>
    Fill your inventory with the following:
    <ul>
        <li>Lost tribe brooch</li>
        <li>Lost tribe book</li>
        <li>Blue dye</li>
        <li>Orange dye</li>
    </ul> 
    <li>Teleport to Falador.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Goblin_Diplomacy" target="_blank">Goblin Diplomacy</a>.</li>
    <li>Continue <a href="http://oldschoolrunescape.wikia.com/wiki/The_Lost_Tribe" target="_blank">The Lost Tribe</a>.</li>
    <br>
    <li>Use the Fishing trawler minigame teleport.</li>
    <li>buy sand/soda ash/seaweed until you have 10k GP left or log in limit</li>
    <br>
    <li>Home teleport to Lumbridge.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/The_Lost_Tribe" target="_blank">The Lost Tribe</a>.</li>
    <li>Make 5 Soft clay.</li>
    Fill your inventory with the following:
    <ul>
        <li>GP</li>
        <li>5 Silver ore</li>
    </ul>  
    <li>Go through the gate to Al-Kharid.</li>
</ol>