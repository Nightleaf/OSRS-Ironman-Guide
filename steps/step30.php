<h4>4.1 - Hosidius Favour & Puro Puro</h4>
<ol>
    <li>Buy 6 compost packs and get 45% hosidius favor.</li>
    <li>Finish 100% favor at the mess hall, lock favour by talking to Hosa in the center of Kourend. (Need to talk to him twice to lock AFAIK)</li>
    <p>
    This is also where you can get 80 cooking at any point you want to, it's up to 210k xp/h with pizzas
    </p>
    <li>Put your house to kourend for herb runs.</li>
    <br>
    <li>Go to the woodcutting guild.</li>
    <li>Cut and make 1700 oak planks.</li>
    <li>Get 50 construction.</li>
    <ul>
        <li>lvl 15, repair bench in the workshop.</li>
        <li>lvl 16, crafting table 1 in the workshop</li>
        <li>lvl 22, oak dining table in the dining room</li>
        <li>lvl 33, oak larder in the kitchen</li>
    </ul>
    <li>Build Oak altar.</li>
    <br>
    <li>Get 83 Hunter.</li>
    <ul>
        <li>29-43 swamp lizards</li>
        <li>43-59 falconry (feel free to do orange sallies if you don't like this, falconry is faster)</li>
        <li>59-67 red salamanders</li>
        <li>67-80 black salamanders</li>
        <p><font color="#853f3d"><b>HCIM: stay at red sallies until 80.</b></font></p>
        <li>80-83 red chinchompas, surround 1 red chin with your traps and just range it if it wanders off</li>
        <p>
		If you want to rush a fire cape before any more slayer then stay here until you have around 4500 red chins for 70 range, or
		you could start doing chins at 77 hunter if you want to stop at 83.
        </p>
    </ul>
</ol> 
<b>Location: </b>Puro Puro
<ol>
    <li>Bring snare and alch runes.</li>
    <li>Get Magic butterfly net and Jar generator.</li>
    <li>Find an eclectic impling spawn, do medium clues until you get any kind of yew bow for temple of ikov.</li>
    <br>
    <li>Go south east corner, catch nature/magpie/ninja/dragon impling, hop and do the same in another world.</li>
    <p>
    	Hold 3 worlds and catch the rare implings as they spawn. &nbsp; You can add a 4th world when you get better at it
    </p>
    <li>Do this until 12 magic logs from the nature implings.</li>
    <p>
    	Don't bother getting a glory from dragons, you'll be doing 80 craft soon anyway. The 83 hunter is mainly for those random D imps.
	If you do happen to get lucky then mount a glory in your house, don't bother using charged glories if you only have 2.
    </p>
