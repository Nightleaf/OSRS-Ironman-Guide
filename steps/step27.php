<h3>Fairy rings, 43 prayer, kingdom and first 99</h3>
<h4>3.3 - Underground Pass & Evil Dave Subquest for RFD</h4>
<ol>
    <li>get Ned as your captain for <a href="http://oldschoolrunescape.wikia.com/wiki/Dragon_Slayer" target="_blank">Dragon Slayer</a>.</li>
    <br>
    <li>Go to Crandor and open the shortcut to Karamja Volcano.</li>
    <li>Go kill Elvarg, use ring of recoil, you can do as many trips as you want, if needed restock food at Tzhaar bank.</li>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Dragon_Slayer" target="_blank">Dragon Slayer</a> and buy a green d'hide top.</li>
    <br>
    <li>Wear gloves and pick up 5 nettles at the yews.</li>
    <li>Continue Evil dave subquest</li>
    <li>Get a slayer task from Vannaka</li>
    <li>Teleport to Lumbridge.</li>
    <br>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Death_to_the_Dorgeshuun" target="_blank">Death to Dorgeshuun</a>. While getting the robes, keep buttons(If you didn't get them while getting the rusty swords)</li>
    <li>Finish Evil dave subquest for <a href="http://oldschoolrunescape.wikia.com/wiki/Recipe_for_Disaster" target="_blank">Recipe for Disaster</a>.</li>
    <li>Thieve Bullseye lantern from Dorgeshuun chests.  (You need a lockpick for the chests)</li>
    <br>
    <li>Get 10 Construction with wooden chairs.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Tears_of_Guthix_(quest)" target="_blank">Tears of Guthix</a>, do this weekly.</li>
    <p>
    Have RC as your lowest skill when you do this, since you won't have ess to train with for a while.
    </p>
    <li>Finish <a href="http://oldschoolrunescape.wikia.com/wiki/Holy_Grail" target="_blank">Holy Grail</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Horror_from_the_Deep" target="_blank">Horror from the Deep</a> quest.</li>
    <p>
    Just mage the dagannoth and hide when it's weak to melee and range. Also hide when you need to eat, it's not difficult with blast
	spells. Use proper food like swordfish and karambwans.
    </p>
    <li>Train slayer until 37 range with a bone crossbow.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Animal_Magnetism" target="_blank">Animal Magnetism</a>.</li>
    <li>Do <a href="http://oldschoolrunescape.wikia.com/wiki/Spirits_of_the_Elid" target="_blank">Spirits of the Elid</a> quest.</li>
    <br>
    <li>Do the <a href="http://oldschoolrunescape.wikia.com/wiki/Underground_Pass" target="_blank">Underground Pass</a> quest.</li>
    <p>
    	You can trap the paladins behind each other same way as the captain from the tourist trap quest.
	For the spider, there are 2 skeletons on the east wall, stand between them and then mage the big one with fire blast. Sometimes
	the little ones attack you but just run a bit north and then back to the same spot, they'll get stuck.
    </p>
</ol>